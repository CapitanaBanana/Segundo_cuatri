ESTADO EQU 41H


ORG 1000H
CADENA DB "HOLIS"
FIN DB 0
ORG 2000H

MOV BX, OFFSET CADENA
;CONFIG HANDSHAKE
IN AL, ESTADO
OR AL, 0H
OUT ESTADO,AL


LOOPEAR: MOV AL, [BX]
 OUT DATO, AL
 INC BX
 CMP BX, OFFSET FIN
 JNZ LOOPEAR
INT 0
END
