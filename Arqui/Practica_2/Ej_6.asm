ORG 1000H
MSJ DB  "INGRESE UN NUMERO:"
FIN DB ?
NUMEROS DB "-CERO UNO DOS TRES CUATRO CINCO SEIS SIETE OCHO NUEVE DIEZ "
ORG 3000H
IMPRIMIR: MOV CH,NUM
CMP NUM, 30H 
JZ OUO
MOV DL,0
OUO:MOV CL, 30H
MOV BX, OFFSET NUMEROS
AVANZAR: ADD BX, 1
CMP BYTE PTR [BX],20H
JNZ NOESESP
INC CL
NOESESP:CMP CH, CL 
JNZ AVANZAR
MOV AL,1 
SEGUIR: INT 7
ADD BX, 1
CMP BYTE PTR [BX],20H
JNZ SEGUIR
RET

ORG 1500H
NUM DB ?
NL DB 0AH
ORG 2000H
MOV DL,0H
AGAIN:MOV BX, OFFSET MSJ
MOV AL, OFFSET FIN-OFFSET MSJ
INT 7
MOV BX, OFFSET NUM
INT 6
MOV AL, 1
INT 7
CMP NUM, 30H
JNZ SALTO
INC DL
CMP DL, 2H
JZ FNI
SALTO: CALL IMPRIMIR
MOV BX, OFFSET NL
MOV AL,1
INT 7
JMP AGAIN
FNI:INT 0
END
